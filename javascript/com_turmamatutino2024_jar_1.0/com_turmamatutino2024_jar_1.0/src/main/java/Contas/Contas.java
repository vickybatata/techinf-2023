
package Contas;


public class Contas {
    
     public int Cod;
    public String Descricao;
    public String Valor;
    public String DtVenc;
    public String DtPag;
    public String Fornecedor;
    public String Categoria;
    public String StatusPag;
    public String MetPag;
    public String Observacoes;

    public int getCod() {
        return Cod;
    }

    public void setCod(int Cod) {
        this.Cod = Cod;
    }

    public String getDescricao() {
        return Descricao;
    }

    public void setDescricao(String Descricao) {
        this.Descricao = Descricao;
    }

    public String getValor() {
        return Valor;
    }

    public void setValor(String Valor) {
        this.Valor = Valor;
    }

    public String getDtVenc() {
        return DtVenc;
    }

    public void setDtVenc(String DtVenc) {
        this.DtVenc = DtVenc;
    }

    public String getDtPag() {
        return DtPag;
    }

    public void setDtPag(String DtPag) {
        this.DtPag = DtPag;
    }

    public String getFornecedor() {
        return Fornecedor;
    }

    public void setFornecedor(String Fornecedor) {
        this.Fornecedor = Fornecedor;
    }

    public String getCategoria() {
        return Categoria;
    }

    public void setCategoria(String Categoria) {
        this.Categoria = Categoria;
    }
    public String getStatusPag() {
        return StatusPag;
    }

    public void setStatusPag(String StatusPag) {
        this.StatusPag = StatusPag;
    }

    public String getMetPag() {
        return MetPag;
    }

    public void setMetPag(String MetPag) {
        this.MetPag = MetPag;
    }

    public String getObservacoes() {
        return Observacoes;
    }

    public void setObservacoes(String Observacoes) {
        this.Observacoes = Observacoes;
    }

}  