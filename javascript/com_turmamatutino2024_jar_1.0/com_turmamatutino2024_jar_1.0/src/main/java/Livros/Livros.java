
package Livros;


public class Livros {
    
    private int Cod;
    private String Titulo;
    private String Autor;
    private String ISBN;
    private String Editora;
    private String AnoPubli;
    private String Genero;
    private String NumPag;
    private String Idioma;
    private String LocBiblioteca;

    public int getCod() {
        return Cod;
    }

    public void setCod(int Cod) {
        this.Cod = Cod;
    }

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String Titulo) {
        this.Titulo = Titulo;
    }

    public String getAutor() {
        return Autor;
    }

    public void setAutor(String Autor) {
        this.Autor = Autor;
    }

    public String getISBN() {
        return ISBN;
    }

    public void setISBN(String ISBN) {
        this.ISBN = ISBN;
    }

    public String getEditora() {
        return Editora;
    }

    public void setEditora(String Editora) {
        this.Editora = Editora;
    }

    public String getAnoPubli() {
        return AnoPubli;
    }

    public void setAnoPubli(String AnoPubli) {
        this.AnoPubli = AnoPubli;
    }

    public String getGenero() {
        return Genero;
    }

    public void setGenero(String Genero) {
        this.Genero = Genero;
    }

    public String getNumPag() {
        return NumPag;
    }

    public void setNumPag(String NumPag) {
        this.NumPag = NumPag;
    }

    public String getIdioma() {
        return Idioma;
    }

    public void setIdioma(String Idioma) {
        this.Idioma = Idioma;
    }

    public String getLocBiblioteca() {
        return LocBiblioteca;
    }

    public void setLocBiblioteca(String LocBiblioteca) {
        this.LocBiblioteca = LocBiblioteca;
    }
    
   
}
