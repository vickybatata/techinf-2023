    public Funcionarios MontarFuncionarios(){
        
        Funcionarios f = new Funcionarios();
        f.setNomeCompleto(jTextFNome.getText());
        f.setCPF(jTextFCPF.getText());
        f.setEndereco(jTextFEndereco.getText());
        f.setTelefone(jTextFTelefone.getText());
        f.setEmail(jTextFEmail.getText());
        f.setDataAdmissao(jTextFDataAdmissao.getText());
        f.setCargo(jTextFCargo.getText());
        f.setSalario(jTextFSalario.getText());
        f.setDepartamento(jTextFDepartamento.getText());
        return f;
    }
    
