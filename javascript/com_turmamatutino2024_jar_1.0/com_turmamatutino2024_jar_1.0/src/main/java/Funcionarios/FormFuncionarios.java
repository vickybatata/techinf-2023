    private void jButtonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarActionPerformed
       Funcionarios f = this.MontarFuncionarios();
       JOptionPane.showMessageDialog(null, f.getNomeCompleto()+" "+f.getCPF()+" "+
               f.getEndereco()+" "+f.getTelefone()+" "+f.getEmail()+" "+
               f.getDataAdmissao()+" "+f.getCargo()+" "+f.getSalario()+" "+
               f.getDepartamento());
    }//GEN-LAST:event_jButtonMostrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Funcionarios f = this.MontarFuncionarios();
        if (f != null && !jTextFCod.getText().trim().isEmpty()) {
            f.setCod(Integer.parseInt(jTextFCod.getText()));
            fcntl.update(f);
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos +"
                    + "corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
         Funcionarios f = this.MontarFuncionarios();
        if (f != null) {
        fcntl.insert(f);
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos +"
                    + " corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       if(!jTextFCod.getText().trim().isEmpty()){
         int cod = Integer.parseInt(jTextFCod.getText());
         fcntl.delete(cod);
        }else{
            JOptionPane.showMessageDialog(null,"Informe novamente...");
        }    }//GEN-LAST:event_btnExcluirActionPerformed

