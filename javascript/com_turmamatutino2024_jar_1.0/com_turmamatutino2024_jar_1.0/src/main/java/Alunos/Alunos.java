
package Alunos;


public class Alunos {
    
    private int Cod;
    private String NomeCompleto;
    private String CPF;
    private String Endereco;
    private String Telefone;
    private String Email;
    private String DataNasc;
    private String SerieAno;
    private String NomeResp;
    private String EscOrigem;

    public int getCod() {
        return Cod;
    }

    public void setCod(int Cod) {
        this.Cod = Cod;
    }

    public String getNomeCompleto() {
        return NomeCompleto;
    }

    public void setNomeCompleto(String NomeCompleto) {
        this.NomeCompleto = NomeCompleto;
    }

    public String getCPF() {
        return CPF;
    }

    public void setCPF(String CPF) {
        this.CPF = CPF;
    }

    public String getEndereco() {
        return Endereco;
    }

    public void setEndereco(String Endereco) {
        this.Endereco = Endereco;
    }

    public String getTelefone() {
        return Telefone;
    }

    public void setTelefone(String Telefone) {
        this.Telefone = Telefone;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getDataNasc() {
        return DataNasc;
    }

    public void setDataNasc(String DataNasc) {
        this.DataNasc = DataNasc;
    }

    public String getSerieAno() {
        return SerieAno;
    }

    public void setSerieAno(String SérieAno) {
        this.SerieAno = SérieAno;
    }

    public String getNomeResp() {
        return NomeResp;
    }

    public void setNomeResp(String NomeResp) {
        this.NomeResp = NomeResp;
    }

    public String getEscOrigem() {
        return EscOrigem;
    }

    public void setEscOrigem(String EscOrigem) {
        this.EscOrigem = EscOrigem;
    }
    

}
