    private void btnAttualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttualizarActionPerformed
        Fornecedores f = this.MontarFornecedores();
        if (f != null && !jTextFCod.getText().trim().isEmpty()) {
            f.setCod(Integer.parseInt(jTextFCod.getText()));
            FornecedoresControle fcntl = new FornecedoresControle();
            fcntl.update(f);
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos +"
                    + "corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAttualizarActionPerformed

    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        Fornecedores f = this.MontarFornecedores();
        if (f != null) {
         fctrl.insert(f);
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos +"
                    + " corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGravarActionPerformed

