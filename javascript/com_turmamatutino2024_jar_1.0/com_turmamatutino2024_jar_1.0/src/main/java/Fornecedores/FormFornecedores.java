    public Fornecedores MontarFornecedores(){
    Fornecedores f = new Fornecedores();
    f.setNomeEmp(jTextFNomeEmpresa.getText());
    f.setCNPJ(jTextFCNPJ.getText());
    f.setEndereco(jTextFEndereco.getText());
    f.setTelefone(jTextFTelefone.getText());
    f.setEmail(jTextFEmail.getText());
    f.setNomeContato(jTextFNomeContato.getText());
    f.setTipoProdServ(jTextFTpProdServ.getText());
    f.setCondPag(jTextFCondicoesPag.getText());
    f.setPrazoEnt(jTextFPrazoEntrega.getText());
    return f;
}
    
