package Fornecedores;


public class Fornecedores {
    
    private int Cod;
    private String NomeEmp;
    private String CNPJ;
    private String Endereco;
    private String Telefone;
    private String Email;
    private String NomeContato;
    private String TipoProdServ;
    private String CondPag;
    private String PrazoEnt;


    public int getCod() {
        return Cod;
    }

    public void setCod(int Cod) {
        this.Cod = Cod;
    }

    public String getNomeEmp() {
        return NomeEmp;
    }

    public void setNomeEmp(String NomeEmp) {
        this.NomeEmp = NomeEmp;
    }

    public String getCNPJ() {
        return CNPJ;
    }

    public void setCNPJ(String CNPJ) {
        this.CNPJ = CNPJ;
    }

    public String getEndereco() {
        return Endereco;
    }

    public void setEndereco(String Endereco) {
        this.Endereco = Endereco;
    }

    public String getTelefone() {
        return Telefone;
    }

    public void setTelefone(String Telefone) {
        this.Telefone = Telefone;
    }    
        
    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getNomeContato() {
        return NomeContato;
    }

    public void setNomeContato(String NomeContato) {
        this.NomeContato = NomeContato;
    }

    public String getTipoProdServ() {
        return TipoProdServ;
    }

    public void setTipoProdServ(String TipoProdServ) {
        this.TipoProdServ = TipoProdServ;
    }

    public String getCondPag() {
        return CondPag;
    }

    public void setCondPag(String CondPag) {
        this.CondPag = CondPag;
    }

    public String getPrazoEnt() {
        return PrazoEnt;
    }

    public void setPrazoEnt(String PrazoEnt) {
        this.PrazoEnt = PrazoEnt;
    }

  
}
