package Produtos;


public class Produto {
    
    private int Cod;
    private String NomeProd;
    private String CodProd;
    private String Descricao;
    private String Preco;
    private String QuantEstoque;
    private String Categoria;
    private String DataValidade;
    private String Fornecedor;
    private String LocEstoque;

    public int getCod() {
        return Cod;
    }

    public void setCod(int Cod) {
        this.Cod = Cod;
    }

    public String getNomeProd() {
        return NomeProd;
    }

    public void setNomeProd(String NomeProd) {
        this.NomeProd = NomeProd;
    }

    public String getCodProd() {
        return CodProd;
    }

    public void setCodProd(String CodProd) {
        this.CodProd = CodProd;
    }

    public String getDescricao() {
        return Descricao;
    }

    public void setDescricao(String Descricao) {
        this.Descricao = Descricao;
    }

    public String getPreco() {
        return Preco;
    }

    public void setPreco(String Preco) {
        this.Preco = Preco;
    }

    public String getQuantEstoque() {
        return QuantEstoque;
    }

    public void setQuantEstoque(String QuantEstoque) {
        this.QuantEstoque = QuantEstoque;
    }

    public String getCategoria() {
        return Categoria;
    }

    public void setCategoria(String Categoria) {
        this.Categoria = Categoria;
    }

    public String getDataValidade() {
        return DataValidade;
    }

    public void setDataValidade(String DataValidade) {
        this.DataValidade = DataValidade;
    }

    public String getFornecedor() {
        return Fornecedor;
    }

    public void setFornecedor(String Fornecedor) {
        this.Fornecedor = Fornecedor;
    }

    public String getLocEstoque() {
        return LocEstoque;
    }

    public void setLocEstoque(String LocEstoque) {
        this.LocEstoque = LocEstoque;
    }

    
    
}
