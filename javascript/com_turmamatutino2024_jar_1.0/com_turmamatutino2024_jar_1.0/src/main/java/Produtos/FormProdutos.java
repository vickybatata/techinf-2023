    private void jButtonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarActionPerformed
        Produto p = this.MontarProduto();
        JOptionPane.showMessageDialog(null, p.getNomeProd()+" "+p.getCodProd()+
                " "+p.getDescricao()+" "+p.getPreco()+" "+p.getQuantEstoque()+
                " "+p.getQuantEstoque()+" "+p.getCategoria()+" "+p.getDataValidade()+
                " "+p.getFornecedor()+" "+p.getLocEstoque());
    }//GEN-LAST:event_jButtonMostrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
         Produto p = this.MontarProduto();
        if (p != null && !jTextFCod.getText().trim().isEmpty()) {
            p.setCod(Integer.parseInt(jTextFCod.getText()));
            ProdutosControle fcntl = new ProdutosControle();
            fcntl.update(p);
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos +"
                    + "corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAtualizarActionPerformed

