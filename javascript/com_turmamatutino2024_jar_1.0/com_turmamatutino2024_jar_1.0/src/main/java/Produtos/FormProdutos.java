    public Produto MontarProduto(){
        Produto p = new Produto();
        p.setNomeProd(jTextFNomeProduto.getText());
        p.setCodProd(jTextFCodigoProduto.getText());
        p.setDescricao(jTextFDescricao.getText());
        p.setPreco(jTextFPreco.getText());
        p.setQuantEstoque(jTextFQuantEstoque.getText());
        p.setCategoria(jTextFCategoria.getText());
        p.setDataValidade(jTextFDataValidade.getText());
        p.setFornecedor(jTextFFornecedor.getText());
        p.setLocEstoque(jTextFLocEstoque.getText());
        return p;
    }
    
