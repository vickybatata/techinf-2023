package Eventos;


public class Eventos {
   
    private int Cod;
    private String NomeEv;
    private String Data;
    private String HorarioInicio;
    private String HorarioTerm;
    private String Local;
    private String Descricao;
    private String PublicoAlvo;
    private String CapacMax;
    private String Organ;

    public int getCod() {
        return Cod;
    }

    public void setCod(int Cod) {
        this.Cod = Cod;
    }

    public String getNomeEv() {
        return NomeEv;
    }

    public void setNomeEv(String NomeEv) {
        this.NomeEv = NomeEv;
    }

    public String getData() {
        return Data;
    }

    public void setData(String Data) {
        this.Data = Data;
    }

    public String getHorarioInicio() {
        return HorarioInicio;
    }

    public void setHorarioInicio(String HorarioInicio) {
        this.HorarioInicio = HorarioInicio;
    }

    public String getHorarioTerm() {
        return HorarioTerm;
    }

    public void setHorarioTerm(String HorarioTerm) {
        this.HorarioTerm = HorarioTerm;
    }

    public String getLocal() {
        return Local;
    }

    public void setLocal(String Local) {
        this.Local = Local;
    }

    public String getDescricao() {
        return Descricao;
    }

    public void setDescricao(String Descricao) {
        this.Descricao = Descricao;
    }

    public String getPublicoAlvo() {
        return PublicoAlvo;
    }

    public void setPublicoAlvo(String PublicoAlvo) {
        this.PublicoAlvo = PublicoAlvo;
    }

    public String getCapacMax() {
        return CapacMax;
    }

    public void setCapacMax(String CapacMax) {
        this.CapacMax = CapacMax;
    }

    public String getOrgan() {
        return Organ;
    }

    public void setOrgan(String Organ) {
        this.Organ = Organ;
    }
    
    
}
